#include <stdio.h>
/**********************************************************************
 * This file parses a given character array into a wav file format
 * @param contents a character array that should contain a wav file
 *********************************************************************/
wav_file* parse(char* contents){
	wav_file* song;
	song = (wav_file*)contents;
	return song;
}
